public class swap_num {
    public static void main(String args[])
    {
            int a = 6;
            int b = 9;
            System.out.println("value of a: " + a + "\nvalue of b: " +b);

            int c = a;
            a = b;
            b = c;
            System.out.println("Swap_value of a: " + a + "\nSwap_value of b: " +b);




    }
}
